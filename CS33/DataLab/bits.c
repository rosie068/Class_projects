/* 
 * twosComp2SignMag - Convert from two's complement to sign-magnitude 
 *   where the MSB is the sign bit
 *   You can assume that x > TMin
 *   Example: twosComp2SignMag(-5) = 0x80000005.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 4
 */
int twosComp2SignMag(int x) {
  /*
   if x positive, then nothing to be done;
   if x negative, then find -x and add TMin;
   */
  int xisPos, xisNeg, TMin;
  xisPos = ((!(x>>31))<<31)>>31;
  xisNeg = ~xisPos;
  TMin = 1<<31;
  return (x & xisPos) + (((~x+1)+TMin) & xisNeg);
}
/*
 * bitParity - returns 1 if x contains an odd number of 0's
 *   Examples: bitParity(5) = 0, bitParity(7) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 4
 */
int bitParity(int x) {
  /*
  Exploits the fact that if x and y have a total of even/odd number of 0s, then x^y has even/odd
  number of bits respectively
  Using a divide and conquer method, when x is 2-bit, we simply check x^(x>>1), the last bit indicated its bit parity 
  when x is 4-bit, we can check the parity of the last 2 bits by >>1, then check the parity of al by >>2
  when x is 8-bit, we first check the parity of the last 2 bits by >>1, then the last four by >>2, then all by >>4
  Similarly for 32-bit numbers, we check the last 2,4,8,16 bits, then all
  */
  int lasttwo, lastfour, lasteight, lastsixteen, result;
  lasttwo = x^(x>>1);     /*parity of last 2 bits*/
  lastfour = lasttwo^(lasttwo>>2);      /*parity of last 4 bits*/
  lasteight = lastfour^(lastfour>>4);
  lastsixteen = lasteight^(lasteight>>8);
  result = lastsixteen^(lastsixteen>>16);    /*last bit indicates parity*/
  return (result&1);
}
