/* 
 * isLess - if x < y  then return 1, else return 0 
 *   Example: isLess(4,5) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isLess(int x, int y) {
	/*
	if equal, return 0 regardless
	if xNegyPos = 1, return 1 regardless
	if xPosyNeg = 1, return 0 regardless
	compute the rest by checking x-y
	*/
  int NotEqual, xyshift, xNegyPos, xPosyNeg, xMinusy, result;
  NotEqual = (!(!(x^y)));     /*if equal, return 0 regardless*/
  xyshift = (x>>31)&(y>>31);
  xNegyPos = (!xyshift)&(x>>31);  /*if this is 1, return 1 regardless*/
  xPosyNeg = (!xyshift)&(y>>31);  /*if this is 1, return 0 regardless*/

  xMinusy = (x+(~y+1));
  result = (xMinusy>>31)&1;
  
  return (NotEqual & (!xPosyNeg) & (xNegyPos | result));
}

/*
 * multFiveEighths - multiplies by 5/8 rounding toward 0.
 *   Should exactly duplicate effect of C expression (x*5/8),
 *   including overflow behavior.
 *   Examples: multFiveEighths(77) = 48
 *             multFiveEighths(-22) = -13
 *             multFiveEighths(1073741824) = 13421728 (overflow)
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 3
 */
int multFiveEighths(int x) {
	/* Exploits right shift n = divided by 2^n
	multiply x by 5 = (x << 2) + x 
	*/
  int product, bias, xSign;
  product = (x<<2)+x;  /*multiply by 5 first to match overflow issue*/
  bias = 7;
  xSign = x>>31;
  return (product + (bias & xSign)) >> 3;
}
