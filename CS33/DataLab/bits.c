/* 
 * isEqual - return 1 if x == y, and 0 otherwise 
 *   Examples: isEqual(5,5) = 1, isEqual(4,5) = 0
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 5
 *   Rating: 2
 */
int isEqual(int x, int y) {
 	/* exploits ability of exclusive-or operator to test if numbers are the same */
	return !(x^y);
}

/* 
 * fitsBits - return 1 if x can be represented as an 
 *  n-bit, two's complement integer.
 *   1 <= n <= 32
 *   Examples: fitsBits(5,3) = 0, fitsBits(-4,3) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int fitsBits(int x, int n) {
	/* if x can be represented by n-bit numbers, then n should preserve
	all n digits if shifted to the left 32-n digits then back
	exploits ~n + 1 = -n and uses the isEqual method 
	*/
	int shift, shift_left, shift_back;
	shift = (~n + 1) + 32;  /*want to shift 31 places*/
	shift_left = x << shift;
	shift_back = shift_left >> shift; 
	return !(shift_back ^ x);
}
